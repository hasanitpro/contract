def build_haftungsbeschraenkung_clause() -> str:
    return (
        "Die verschuldensunabhängige Haftung des Vermieters für bei Vertragsabschluss vorhandene Sachmängel gem. § 536a Abs. 1 Satz 1 Alt. 1 BGB wird ausgeschlossen, es sei denn, es handelt sich um Verletzung von Leben, Körper, Gesundheit. Ansonsten besteht ein Schadensersatzanspruch des Mieters für anfängliche Mängel nur dann, wenn der Vermieter deren Vorhandensein oder Nichtbeseitigung zu vertreten hat."
    )
